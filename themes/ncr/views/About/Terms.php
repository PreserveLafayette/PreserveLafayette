<div class="textContent">
	
	<div>
		<b>Akari</b><br/>
		The Japanese word <i>Akari</i> means light as illumination, but also implies the idea of weightlessness, and was chosen by Noguchi to identify the lamps he designed and produced in the traditional Gifu methods of paper-lantern construction.
	</div>
	<div>
		<b>Anonymous</b><br/>
		A collection credit given in an artwork’s provenance to indicate unconfirmed or unidentified private ownership. <i>Anonymous</i> may indicate that a former owner is unknown, or that a known person(s) has not indicated to the project how their collection credit should appear.
	</div>
	<div>
		<b>Base </b><br/>
		A detachable structure designed by Noguchi and used for the artwork’s display and presentation that is essential to the presentation of the artwork.
	</div>
	<div>
		<b>Bibliography</b><br/>
		The list of literature concerning an artwork, or the complete list of published and unpublished writing concerning the life and work of Isamu Noguchi.
	</div>
	<div>
		<b>Cast Edition</b><br/>
		An artwork manufactured via a process during which molten metal or liquid plaster or clay is poured into a mold to create one or multiple reproductions of an original design represented by the mold.
	</div>
	<div>
		<b>Constructed Edition</b><br/>	
		An artwork manufactured in multiple to replicate exactly an original design without the use of a mold-casting process.
	</div>
	<div>
		<b>Design</b><br/>
		Generally, a term applied to objects and concepts created by Noguchi for the home, interior, or for daily use, ranging from mass-produced, limited-production, and unique objects to environments.
	</div>
	<div>
		<b>Destroyed</b><br/>
		An original artwork that has been entirely demolished by the artist or by others, or significantly altered from its original state or condition without the artist’s involvement or approval. Destroyed artworks are confirmed as destroyed via historical evidence of destruction. If exact conditions are not fully evident, an artwork suspected of being destroyed is listed as lost.
	</div>
	<div>
		<b>Discarded</b><br/>
		An object rejected by the artist as an artwork.
	</div>
	<div>
		<b>Edition</b><br/>
		Used when categorizing an artwork: an original artwork by Noguchi that, through fabrication, is an exact replica of an original artwork.
		<br/><br/>As information on an artwork entry: the series to which an artwork is related, which may include an original artwork, editioned copies, and secondary versions of the original in the same or different material.
	</div>
	<div>
		<b>Element</b><br/>
		Within a single artwork: a separate but dependent or relational component in a composition, which in concert with other elements constitutes a complete artwork. 
		<br/><br/>Also used to refer to an individual artwork that is part of a larger project, such as an element of a Noguchi-designed stage set, or sculpture in one of the artist’s landscape designs.
	</div>
	<div>
		<b>Inscribed</b><br/>
		Marked by the artist with a carving or engraving on the artwork’s surface to indentify the work’s authorship.
	</div>
	<div>
		<b>Lost</b><br/>
		Generally, an unlocated work thought to have been destroyed, for which evidence to confirm this belief is incomplete.  Artworks listed as “lost” in <i>The Sculpture of Isamu Noguchi, 1929–1979: A Catalogue</i>, by Nancy Grove and Diane Botnick (Garland, 1980), and that have not since been located, retain this term in place of a current collection. Works unlocated since the publication of this volume are listed, instead, as “Whereabouts Unknown.”
	</div>
	<div>
		<b>Lunar</b><br/>
		The word <i>Lunar</i> was chosen by Noguchi to describe artworks created, generally in the 1940s, which incorporate electric light components under thin burlap-reinforced magnesite structures.
	</div>
	<div>
		<b>Magnesite</b><br/>
		A cement-like construction material compound of magnesium, carbon, and oxygen (MgO Cl2) traditionally used for industrial flooring. Noguchi began using magnesite with burlap reinforcing for the construction of sculpture in the late 1930s, finding it superior to traditional cement for thin constructions. 
	</div>
	<div>
		<b>Manufactured Object</b><br/>
		A design conceived of by Noguchi that was mass produced or intended for mass production.
	</div>
	<div>
		<b>Model</b><br/>
		A preparatory object for an artwork or project.
	</div>
	<div>
		<b>Pedestal</b><br/> 
		A detachable structure used for the presentation of an artwork that is inessential to the presentation of the work, and/or interchangeable.
	</div>
	<div>
		<b>Posthumous</b><br/>
		Objects or elements of objects conceived of by Noguchi but executed or completed partially or wholly after his death on December 30, 1988.
	</div>
	<div>
		<b>Private Collection</b><br/>
		A collection credit designated by an artwork’s owner or owners who wish to remain unidentified.
	</div>
	<div>
		<b>Project </b><br/>
		A large-scale artistic enterprise, usually commissioned, including but not limited to Noguchi’s creation of and proposals for spaces such as gardens, plazas, playgrounds, public sculptures, stage and theater designs, and interior designs. 
	</div>
	<div>
		<b>Provenance</b><br/>
		The history of an artwork’s ownership and whereabouts, including past collections, auctions, and consignments.
	</div>
	<div>
		<b>Published</b><br/>
		The research status of an artwork, exhibition, or bibliography entry for which research is considered finalized. Artworks are categorized as published once the work has been examined and measured by project staff, its provenance and historical attributes fully updated, and publishable photography obtained. 
	</div>
	<div>
		<b>Reconstruction </b><br/>
		An artwork by Noguchi that has been partially rebuilt or reformed using new materials or processes not original to the artwork.
	</div>
	<div>
		<b>Reproduction </b><br/>
		A version of an original artwork by Noguchi that has been entirely rebuilt or reformed, often created as an example of the original for exhibition, or representation after the original was lost or destroyed.
	</div>
	<div>
		<b>Research Pending</b><br/>
		The research status of an artwork, exhibition, or bibliography entry for which research is ongoing. While an entry’s status is research pending, limited information is provided. Once research is completed, full information is provided and the status of the entry is changed to published. 
	</div>
	<div>
		<b>Research Status</b><br/>
		The state of research being conducted on an individual artwork, exhibition, or bibliography entry, classified as either published or research pending.
	</div>
	<div>
		<b>Signed</b><br/>
		Marked by the artist with pencil, pen, or other writing implement to indentify the work’s authorship.
	</div>
	<div>
		<b>Site Specific </b><br/>
		A sculpture or project existing in an environment that is an integral to the artwork’s composition. Site-specific works cannot be moved to a different location without the artwork being destroyed.
	</div>
	<div>
		<b>Study</b><br/>
		A preliminary or working design for an artwork or project, or a quick drawing/preparatory sketch.
	</div>
	<div>
		<b>Unrealized </b><br/>
		A concept that was not ultimately constructed, or a work not constructed to the original specifications.
	</div>
	<div>
		<b>Unfinished </b><br/>
		An artwork that was not completed at the time of Noguchi’s death.
	</div>
	<div>
		<b>Version </b><br/>
		An artwork by Noguchi that is a physical variation by size or material of an original concept.
	</div>
	<div>
		<b>Whereabouts Unknown </b><br/>
		Artworks for which the present collection or location is indeterminable .
	</div>
</div>