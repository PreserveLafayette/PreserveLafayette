	<div>
		<b>Artworks</b>
		<br/>When research is complete, <i>The Isamu Noguchi Catalogue Raisonn&eacute;</i> is expected to encompass all known artworks by Isamu Noguchi, ranging from sculptures, drawings, and models to architectural spaces, stage sets, and manufactured designs.
	</div>
	<div>
		Ongoing inclusion of artworks in <i>The Isamu Noguchi Catalogue Raisonn&eacute;</i> is contingent upon the artwork having been examined and measured by project staff, its provenance and historical attributes fully updated, and publishable photography obtained. 
	</div>
	<div>
		<u>Artworks included encompass the following</u>:
		<ul>
			<li>All distinct artworks conceived of and created by Isamu Noguchi during his lifetime, including but not limited to sculptures, drawings, models, and studies</li>
			<li>All environments conceived of and created by Noguchi during his life time, including but not limited to stage sets, architectural spaces, and site-specific, multi-element arrangements</li>
			<li>Manufactured designs conceived of by Noguchi, including concepts for mass-produced designs and individual manufactured objects fabricated in a limited production</li>
			<li>Artworks designed by Noguchi and fabricated by others in accordance with the artistâ€™s instructions or designs</li>
			<li>Artworks by Noguchi considered lost or destroyed when there is adequate documentation of the work's prior existence and authenticity</li>
			<li>Reproductions of original artworks by Noguchi made or authorized by the artist during his lifetime</li>
			<li>Artworks by Noguchi that have been reconstructed by the artist or others with information on their original and altered states</li>
		</ul>
	</div>
	<div>
		<u>Objects not included fall under the following general limitations</u>:
		<ul>
			<li>Artworks of questionable attribution or authenticity</li>
			<li>Posthumous artworks</li>
			<li>Reproductions of original artworks not made or authorized by Noguchi</li>
			<li>Objects discarded by Noguchi</li>
			<li>Blueprints drafted by Noguchi or his associates unless significantly marked with unique qualities such as hand-made illustrations by the artist</li>
			<li>Molds for cast objects</li>
			<li>Photographs taken by Noguchi and ephemera related to the artist</li>
			<li>Works for hire and works executed to the specifications of another person's instructions or designs where Noguchi had no influence on the original concept or design</li>
			<li>Individual manufactured objects mass-produced and intended for distribution to a mass market (the design concept for a manufactured object is instead represented by a single entry to represent the entire production run, without collection or provenance information for individual examples)</li>
		</ul>
	</div>
	<div>
		<b>Exhibitions</b>
		<br/>The list of exhibitions includes solo and group exhibitions that featured the work of Isamu Noguchi during his lifetime, as well as selected exhibitions following Noguchi's death.
	</div>
	<div>
		<u>Exhibitions not included fall under the following general limitations</u>:
		<ul>
			<li>Exhibitions of photography, ephemera, and other objects that are not included in the catalogue</li>
			<li>Dealer and auction house showroom presentations of works</li>
			<li>Selections from public or private collections and permanent or semi-permanent displays of objects</li>
		</ul>
	</div>
	<div>
		<b>Bibliography</b><br/>
		The bibliography is comprised of writings by and about Isamu Noguchi, including books, essays, newspaper and journal articles, exhibition catalogues, monographs, and other writings such as doctoral dissertations. Literature that does not make significant reference to artworks in <i>The Isamu Noguchi Catalogue Raisonn&eacute;</i> may also be included when relevant to the understanding of Noguchi's career.
	</div>
	<div>
	<u>Literature not included fall under the following general limitations</u>:
		<ul>
			<li>Advertisements and other publicity publications</li>
			<li>Articles and other information on The Noguchi Museum and its activities following Isamu Noguchi's death</li>
			<li>Institutional publications such as annual reports or recent acquisition publications</li>
			<li>Auction catalogues</li>
		</ul>
	</div>
