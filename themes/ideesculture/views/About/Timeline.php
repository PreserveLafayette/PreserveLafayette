<h1>Chronologie :  50 oeuvres d'art &agrave; voir dans votre vie</h1>
<p>Pawtucket supporte facilement l'intégration de widgets, tel que Google Agenda, MindMapping,
flux twitter ou facebook...</p>
<p>Voici un exemple de chronologie réalisée de manière autonome à partir d'un projet distinct de CollectiveAccess : <a href="http://www.simile-widgets.org/timeline/">Simile Timeline</a>.<br/>
Cliquez sur une des oeuvres pour afficher plus d'informations. Utilisez les flèches du clavier ou le glisser-déplacer pour faire dérouler la frise chronologique, pour
naviguer facilement de 3000 av. J.-C. à nos jours.</p>

