<h1>A propos de cette démonstration CollectiveAccess</h1>
<div class="textContent">
<div>
Ce catalogue public d'oeuvres est une version de démonstration
de Pawtucket, une des applications de <a
href="http://www.collectiveaccess.org">CollectiveAccess</a>,
solution opensource pour la gestion des collections.
</div>
<div>
L'utilisation de CollectiveAccess est fortement liée à son
paramétrage, les champs de stockage des données, les interfaces de
saisie, le vocabulaire d'indexation... sont complètement adaptables.
</div>
<div>
Nous disposons de trois bases de démonstrations francophones,
correspondant à trois structures différentes :
<ul>
<li>
un jeu de données test d'environs 50 objets dans une structure
simple, dérivée du Dublin Core</li>
<li>
une installation basée sur le profil SPECTRUM (standard de
catalogage UK très détaillé)</li>
<li>un profil que nous créons pour correspondre au standard JOCONDE
(Direction des Musées de France), c'est <b>cette base-ci</b>.</li>
</ul>
</div>
