<a name="library"></a><H2>Libraries and Archives</H2>
<ul class="resourcesList">
	<li>
		<a href="http://legacy.www.nypl.org/research/chss/spe/rbk/mss.html" target="_blank">New York Public Library, Manuscripts and Archives Division</a>
		<div class="resourcesDescription">
			The New York Public Library holds the <a href="http://catalog.nypl.org/record=b11686556~S1" target="_blank">New York World's Fair 1939 and 1940 Incorporated records</a>, consisting of 2508 boxes, 42 volumes, 18 tubes, and 10 sound recordings presenting a comprehensive view of all aspects of the fair.  NYPL has made selections from its holdings available through the <a href="http://www.nypl.org/biblion" target="_blank">Biblion</a> app for iPad and through <a href="http://itunes.apple.com/us/itunes-u/1939-40-new-york-worlds-fair/id430390294" target="_blank">iTunes</a>.
		</div><!-- end resourcesDescription -->
	</li>
	<li>
		<a href="http://catalog.loc.gov/cgi-bin/Pwebrecon.cgi?DB=local&PAGE=First" target="_blank">Library of Congress Online Catalog</a>
		<div class="resourcesDescription">
			Results from the Library of Congress using the keywords "New York World's Fair 1939,"  including fiction and nonfiction books.  For your own search in the Library of Congress catalog <a href="http://catalog.loc.gov/cgi-bin/Pwebrecon.cgi?DB=local&PAGE=First" target="_blank">here</a>, enter "New York World's Fair (1939-1940)" in the Search Text box, then in Search Type click on Subject Keyword. 
		</div><!-- end resourcesDescription -->
	</li>
	<li>
		<a href="http://memory.loc.gov/ammem/collections/gottscho/" target="_blank">Library of Congress American Memory, Gottscho-Schleisner Inc. Collection</a>
		<div class="resourcesDescription">
			In the Search box enter "New York World's Fair" to search The Gottscho-Schleisner Collection of photographs, which includes digitized representations.
		</div><!-- end resourcesDescription -->
	</li>
	<li>
		<a href="http://collections.mcny.org/MCNY/C.aspx?VP3=CMS3&VF=MNY_HomePage#/ViewBox_VPage&VBID=24UP1GRXL3RD&CT=Search" target="_blank">Museum of the City of New York</a>
		<div class="resourcesDescription">
			Approximately 400 digitized images of the New York World's Fair including photographs and watercolors.
		</div><!-- end resourcesDescription -->
	</li>
	<li>
		<a href="http://si-pwebsrch02.si.edu/search?q=new+york+world%27s+fair&site=aaamain&client=aaamain&proxystylesheet=aaamain&output=xml_no_dtd" target="_blank">Smithsonian Archives of American Art</a>
		<div class="resourcesDescription">
			Includes photogarphs,oral histories and other documents on NYWF 1939-1940.
		</div><!-- end resourcesDescription -->
	</li>
	<li style="clear:left;">
		<a href="http://siris-archives.si.edu/ipac20/ipac.jsp?session=1A99516N609J1.293&profile=all&uri=link=3100006~!16324~!3100001~!3100002&aspect=Browse&menu=search&ri=4&source=~!siarchives&term=New+York+World%27s+Fair+%281939-1940%29&index=NAMEP" target="_blank">Smithsonian Archives Center, National Museum of American History</a>
		<div class="resourcesDescription">
			Film holdings include <i>Alice R. Hillis World's Fair Film, 1939</i>, a 16 mm. Kodachrome reel by <a href="http://siris-archives.si.edu/ipac20/ipac.jsp?&profile=all&source=~!siarchives&uri=full=3100001~!140692~!0#focus" target="_blank">Alice Rogers Hillis</a>, and <i>Memories of the Fair, 1939-1940</i>, an 8 mm. film shot and edited by <a href="http://siris-archives.si.edu/ipac20/ipac.jsp?uri=full=3100001~!237136!0" target="_blank">Frank Pezzano</a> with hand-drawn intertitles.
		</div><!-- end resourcesDescription -->
	</li>
	<li>
		<a href="http://collections.si.edu/search/results.jsp?q=New+York+World%27s+Fair" target="_blank">Smithsonian Institution Collections Search Center</a>
		<div class="resourcesDescription">
			Smithsonian Institution museums including National Museum of American History, Kenneth E. Behring Center, aggregated returns with digital images of photographs, drawings, objects.
		</div><!-- end resourcesDescription -->
	</li>
