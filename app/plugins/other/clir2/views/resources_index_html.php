<a name="video"></a><H2>Digital Video Online</H2>
<ul class="resourcesList">
	<li>
		<a href="http://www.archive.org/search.php?query=creator%3A%22Medicus%20%28P.%29%22%20AND%20%28%28collection%3Aprelinger%20OR%20mediatype%3Aprelinger%29%20AND%20-mediatype%3Acollection%20AND%20firstTitle%3AA%29" target="_blank">Amateur film: Medicus collection, New York World's Fair, 1939-40</a>
		<div class="resourcesDescription">
			Eighteen digital video files for streaming and download from the Prelinger Archives on the Internet Archive, approximately 6 hours of viewing. The Prelinger Archives also contains amateur film by R.A. Wathen, and at least one unidentified amateur creator. In addition, commercial films from the fair: <a href="http://www.archive.org/details/middleton_family_worlds_fair_1939" target="_blank">The Middleton Family at the New York World's Fair</a>, <a href="http://www.archive.org/details/LeaveItt1940" target="_blank">Leave It to Roll-Oh</a>, and <a href="http://www.archive.org/details/ToNewHor1940" target="_blank">To New Horizons</a>.
		</div><!-- end resourcesDescription -->
	</li>
	<li>
		<a href="http://www.youtube.com/robertwmartens#p/u/0/Q0lGDVnWVnk" target="_blank">Amateur film: Gustave A. Martens, New York World's Fair</a>
		<div class="resourcesDescription">
			Posted to YouTube by Robert W. Martens, G.A. Martens's grandson. Eight selections by G.A. Martens at the NYWF (stills and QuickTime video), <a href="http://www.webnik.com/homemovies/39wf/index.html" target="_blank">http://www.webnik.com/homemovies/39wf/index.html</a>.  
		</div><!-- end resourcesDescription -->
	</li>
	<li>
		<a href="http://itunes.apple.com/us/itunes-u/1939-40-new-york-worlds-fair/id430390294" target="_blank">Amateur film: H. Earl Hoover's Home Movies, New York World's Fair 1939</a>
		<div class="resourcesDescription">
			Visit the Fair with H. Earl Hoover, a member of the Amateur Cinema League who donated his 22-minute home movie to the New York Public Library collection.  The complete film is available for free download at <a href="http://itunes.apple.com/us/itunes-u/1939-40-new-york-worlds-fair/id430390294" target="_blank">iTunes</a>.  Excerpts from the film are also available online through <a href="http://www.youtube.com/user/Biblionnypl" target="_blank">NYPL's Biblion channel</a> at YouTube.
