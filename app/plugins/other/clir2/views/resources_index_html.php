<H1>Selected New York World's Fair 1939-1940 Resources</H1>
<p>
	This is an introductory roundup of resources relating to the New York World's Fair, 1939-1940.  Many of the books contain bibliographies and the Websites, in particular Alan Anderson's site and the Forum at Peace Through Understanding, are full of additional links and leads.
</p>
