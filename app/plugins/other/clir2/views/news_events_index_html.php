		<H3>Home Movie Day</H3>
		Don't throw your old Kodachrome and black & white family films away. Instead, bring them to the ninth annual <a href="http://www.queensmuseum.org/dont-throw-your-films-away-bring-them-to-home-movie-day" target="_blank">International Home Movie Day</a>, a free event held at Queens Museum of Art on <b>Saturday, October 15, from 1 - 5 pm</b>, films to be accepted for inspection one hour before event starts. This year, the featured subject is the 1939-1940 New York World's Fair, so if you have any reels of the fair, bring them along. Twentieth century amateur films are of increasing interest to scholars in many areas of study; films made by women creators and families of color have been under-represented in many research collections and are most welcome at Home Movie Day.
		<br/><br/>Moving image preservation specialists, including archivists from Northeast Historic Film in Bucksport, Maine, and the NYU Moving Image Archiving and Preservation program, will help the public to learn about, enjoy, and rescue amateur films. Film preservation experts will examine your films and–if they're in screenable condition–prepare them for sharing with the audience.
		<br/><br/>It's a day of discovery, as people often bring boxes and cans of films that have not been seen by present generations. <a href="http://www.homemovieday.com/" target="_blank">Home Movie Day</a> is an international event taking place at the same time in cities across the U.S. and the world, dedicated to the viewing and the preservation of home movies shot on film. Though digital media dominate the market, none of these formats have the quality or longevity of the Super 8, 8 mm, and 16 mm films you may find in your closets, attic, or basement. Home Movie Day is celebrated throughout North American, in Japan, the UK, Wales, and Mexico.
		<br/><br/>Preservation specialists will discuss why transferring your films to video or digital media doesn't mean they'll last forever. All films will be inspected and projected on a first-come, first-served basis. Damaged films will not be screened. No films of your own? Just come and watch the show!
		<br/><br/>The 1939-1940 New York World's Fair was closely associated with nontheatrical film. Stephen F. Voorhees was the Board of Design chairman for the fair. Voorhees, an amateur cinematographer himself (although his films are not known to survive), was also the president of the Amateur Cinema League, founded in New York in 1926. “The New York World's Fair was an immense amateur cinephile magnet. Many of the reels shot over the summers of 1939 and 1940 have turned up but an unknown number are still in people's homes,” says Brian Graney, media cataloger for Amateur Filmmakers Record the New York World's Fair and Its Period, a project of <a href="http://www.oldfilm.org/" target="_blank">Northeast Historic Film</a>, <a href="http://www.queensmuseum.org" target="_blank">Queens Museum of Art</a> and <a href="http://www.eastmanhouse.org/" target="_Blank">George Eastman House International Museum of Photography and Film</a>. Visit the project website at <a href="www.fairfilm.org">www.fairfilm.org</a>.
		<br/><br/>Home Movie Day is a free event and open to all, and donations are accepted.
		<br/><br/>For questions and information regarding Home Movie Day at QMA, please email <a href="mailto:preddy@queensmuseum.org">preddy@queensmuseum.org</a> or call 718.592.9700×222.
