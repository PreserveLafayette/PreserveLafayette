		<H3>Designing Tomorrow</H3>
		<a href="http://www.nbm.org/exhibitions-collections/exhibitions/worlds-fairs.html" target="_blank">Designing Tomorrow</a>, the exhibition on America's World's Fairs of the 1930s at the National Building Museum in Washington, DC, has been extended through September 5. The original exhibition is the first to consider the impact of all six American world fairs of the depression era on the popularization of modern design and the creation of modern consumer culture.  
		<br/><br/>
			<a href="http://www.nbm.org/biographies/staff/deborah-moore-sorensen.html" target="_blank">Deborah Sorensen</a>, one of the exhibition's curators, serves as liaison with the NHF, QMA and GEH Amateur Filmmakers project, bringing her knowledge of fair history and interest in film and the built environment. She curated the moving image media in the exhibition, selecting from archives around North America, including clips from the Robert Decker Collection and the Wilbur Senter Collection at Northeast Historic Film.  
		<br/><br/>
			Designing Tomorrow brings together nearly 200 artifacts from the six fairs. In galleries on the ground floor of the magnificent <a href="http://www.nbm.org/exhibitions-collections/exhibitions/building-a-landmark.html" target="_blank">1881 Pension Building</a>, Designing Tomorrow is organized into seven thematic galleries: Welcome to the Fairs, A Fair-going Nation, Building a Better Tomorrow, Better Ways to Move, Better Ways to Live, Better Times, and Legacies.
		<br/><br/>
			The companion <a href="http://www.worldcat.org/oclc/494981466" target="_blank">book</a> is edited Robert W. Rydell, consulting curator of Designing Tomorrow and Michael P. Malone Professor of History at Montana State University, and Laura Burd Schiavo, curator of Designing Tomorrow and assistant professor at the George Washington University.
